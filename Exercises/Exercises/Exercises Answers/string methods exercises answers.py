# EX 1: Convert the string to uppercase
string1 = "hello world"
result1 = string1.upper()
print("1. Uppercase:", result1)

# EX 2: Convert the string to lowercase
string2 = "HELLO WORLD"
result2 = string2.lower()
print("2. Lowercase:", result2)

# EX 3: Capitalize the first letter of the string
string3 = "python EXs"
result3 = string3.capitalize()
print("3. Capitalize:", result3)

# EX 4: Count the occurrences of 'o' in the string
string4 = "hello world"
result4 = string4.count('o')
print("4. Count of 'o':", result4)

# EX 5: Check if the string ends with 'world'
string5 = "hello world"
result5 = string5.endswith('world')
print("5. Ends with 'world'?", result5)

# EX 6: Replace 'world' with 'Python' in the string
string6 = "hello world"
result6 = string6.replace('world', 'Python')
print("6. Replace 'world' with 'Python':", result6)

# EX 7: Split the string by spaces
string7 = "python EXs"
result7 = string7.split()
print("7. Split by spaces:", result7)

# EX 8: Join a list of strings with '-'
string8 = "-".join(['python', 'EXs'])
print("8. Join with '-':", string8)

# EX 9: Check if the string contains only alphabetic characters
string9 = "PythonEXs"
result9 = string9.isalpha()
print("9. Contains only alphabetic characters?", result9)

# EX 10: Reverse the string
string10 = "Python EXs"
result10 = string10[::-1]
print("10. Reversed string:", result10)

